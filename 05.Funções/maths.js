Math.round() //arredonda 

Math.round(4.3)   //4
Math.round(3.85)  //4
Math.round(2.5)   //3 (para cima)



Math.ceil()  //arredonda para o valor mais alto
Math.floor() // arredonda para o valor mais baixo

Math.ceil(5.2)    //6
Math.floor(5.2)   //5


Math.trunc()   //desconsidera números decimais

Math.trunc(4.3)   //4
Math.trunc(4.8)   //4



Math.pow()  //exponenciação de 2 numeros
Math.sqrt() //raíz quadrada

Math.pow(4 , 2)     //16
Math.pow(2.5 , 1.5) //3.95

Math.sqrt(64)   //8


Math.min()  //menor valor entre os existentes
Math.max()  //maior valor entre os existentes

Math.min(0, 150, 30, 20, -8, -200)   //-200
Math.max(0, 150, 30, 20, -8, -200)   //150



Math.random()  //retorna qualquer valor sortido entre 0 e 1

